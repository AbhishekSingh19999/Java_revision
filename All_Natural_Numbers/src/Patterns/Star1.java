package Patterns;

public class Star1 {

	public static void main(String[] args) {
		
        System.out.println("Star pattern for Triangle"); //right lower hypo..
		
		for(int i=1;i<=5;i++)
		{
			for(int j=5;j>=i;j--)
			{
				System.out.print("*");
			}
			System.out.println();	     
		}

	}




	}


